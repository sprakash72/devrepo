package com.launchwindows.model.weatherinfo;

public class WindSection {
	Float speed;
	Float deg;

	public Float getDeg() {
		return deg;
	}

	public void setDeg(Float deg) {
		this.deg = deg;
	}

	public Float getSpeed() {
		return speed;
	}

	public void setSpeed(Float speed) {
		this.speed = speed;
	}
	
}
